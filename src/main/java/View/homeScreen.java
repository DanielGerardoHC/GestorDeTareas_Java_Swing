    private void btnEliminarTareaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarTareaMouseClicked
        if(selectedTask != null)
        {
        if(JOptionPane.showConfirmDialog(this,"Seguro de eliminar la tarea: "+tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),1).toString(),
                                       "Eliminar Tarea", 
                                       JOptionPane.YES_NO_OPTION,
                                       JOptionPane.WARNING_MESSAGE) == JOptionPane.YES_OPTION)
        {   
            if(DAOtask.eliminar(this.selectedTask.getId_task()))
            {
                JOptionPane.showMessageDialog(null, "Tarea eliminada con exito");
                CargarTabla("Pendiente");
            }
            else  {
                JOptionPane.showMessageDialog(null, "Oh no! algo salio mal");
            }     
        }
        }
        else
        {
         JOptionPane.showMessageDialog(null, "Selecciona una tarea");
        }
    }//GEN-LAST:event_btnEliminarTareaMouseClicked

