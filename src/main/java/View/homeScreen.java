    private void btnMarcarTareaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMarcarTareaMouseClicked
       if(selectedTask != null)
       {       
        if(tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),4).toString().equalsIgnoreCase("Completada"))
        {
            JOptionPane.showMessageDialog(null, "La tarea ya fue completada");
            return;
        }
        if(JOptionPane.showConfirmDialog(this,"Seguro de marcar la tarea: "+tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),1).toString()+ " Como completada?",
                                       "Completar Tarea", 
                                       JOptionPane.YES_NO_OPTION,
                                       JOptionPane.WARNING_MESSAGE) == JOptionPane.YES_OPTION)
        {   
            
            selectedTask.setState(new State());           
            selectedTask.getState().setId_state(2);
            if(DAOtask.modificar(selectedTask, selectedTask.getId_task()))
            {
                JOptionPane.showMessageDialog(null, "Tarea Completada con exito");
                CargarTabla("Pendiente");
            }
            else  {
                JOptionPane.showMessageDialog(null, "Oh no! algo salio mal");
            }     
        }
       }   
       else
       {
        JOptionPane.showMessageDialog(null, "Selecciona una tarea");
       }
    }//GEN-LAST:event_btnMarcarTareaMouseClicked

    private void tlbPendingTaskMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tlbPendingTaskMouseClicked
         this.selectedTask = new Task();
         this.selectedTask.setId_task(Integer.parseInt(tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(), 0).toString()));
         this.selectedTask.setTask_name(tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),1).toString());
         this.selectedTask.setTask_description(tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),2).toString());
         this.selectedTask.setTask_date(tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),3).toString());
         this.selectedTask.setState(new State());
         this.selectedTask.getState().setState_name(tlbPendingTask.getValueAt(tlbPendingTask.getSelectedRow(),4).toString());
    }//GEN-LAST:event_tlbPendingTaskMouseClicked

